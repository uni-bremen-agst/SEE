namespace SEE.Net
{
    /// <summary>
    /// Contains all files needed for a SEECitySnapshot.
    /// </summary>
    public class SEECitySnapshot
    {
        /// <summary>
        /// The path of the configuration file.
        /// </summary>
        public string ConfigPath;

        /// <summary>
        /// The path of the graph file.
        /// </summary>
        public string GraphPath;

        /// <summary>
        /// The path of the layout file.
        /// </summary>
        public string LayoutPath;

        /// <summary>
        /// The name of the code city.
        /// </summary>
        public string CityName;
    }
}
