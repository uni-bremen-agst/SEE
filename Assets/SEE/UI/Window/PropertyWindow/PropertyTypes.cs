        public const string StringAttributes = "String";

        /// <summary>
        /// Name for integer attribute type.
        /// </summary>
        public const string IntAttributes = "Integer";

        /// <summary>
        /// Name for float attribute type.
        /// </summary>
        public const string FloatAttributes = "Float";
    }
}
