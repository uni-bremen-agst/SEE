namespace Dissonance
{
    /// <summary>
    /// Defines tradeoff of audio quality vs performance
    /// </summary>
    public enum AudioQuality
    {
        /// <summary>
        /// Lowest quality but best performance
        /// </summary>
        Low,

        /// <summary>
        /// Average quality and performance
        /// </summary>
        Medium,

        /// <summary>
        /// Best quality but worst performance
        /// </summary>
        High
    }
}
